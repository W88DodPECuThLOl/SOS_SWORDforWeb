"use strict";

/**
 * S-OSのキーコード
 */
class SOSKeyCode {

	/**
	 * S-OSのキーコード
	 * 0
	 * @type {number}
	 */
	static NUL = 0x00;

	/**
	 * S-OSのキーコード
	 * CLS
	 * @type {number}
	 */
	static CLS = 0x0C;

	/**
	 * S-OSのキーコード
	 * Enterキー
	 * @type {number}
	 */
	static CR = 0x0D;

	/**
	 * S-OSのキーコード
	 * Breakキー
	 * @type {number}
	 */
	static BRK = 0x1B;

	/**
	 * S-OSのキーコード
	 * スペース
	 * @type {number}
	 */
	static SPACE = 0x20;
	
	/**
	 * S-OSのキーコード
	 * コロン
	 * @type {number}
	 */
	static COLON = 0x3A;
};
