"use strict";

/**
 * S-OSのキーコード
 */
class SOSKeyCode {

	/**
	 * S-OSのキーコード
	 * 0
	 * @type {number}
	 */
	static NUL = 0x00;

	/**
	 * S-OSのキーコード
	 * CLS
	 * @type {number}
	 */
	static CLS = 0x0C;

	/**
	 * S-OSのキーコード
	 * Enterキー
	 * @type {number}
	 */
	static CR = 0x0D;

	/**
	 * S-OSのキーコード
	 * Breakキー
	 * @type {number}
	 */
	static BRK = 0x1B;

	/**
	 * S-OSのキーコード
	 * 右カーソルキー
	 * @type {number}
	 */
	static ArrowRight = 0x1C;

	/**
	 * S-OSのキーコード
	 * 左カーソルキー
	 * @type {number}
	 */
	static ArrowLeft = 0x1D;

	/**
	 * S-OSのキーコード
	 * 上カーソルキー
	 * @type {number}
	 */
	static ArrowUp = 0x1E;

	/**
	 * S-OSのキーコード
	 * 下カーソルキー
	 * @type {number}
	 */
	static ArrowDown = 0x1F;

	/**
	 * S-OSのキーコード
	 * スペース
	 * @type {number}
	 */
	static SPACE = 0x20;
	
	/**
	 * S-OSのキーコード
	 * コロン
	 * @type {number}
	 */
	static COLON = 0x3A;
};
