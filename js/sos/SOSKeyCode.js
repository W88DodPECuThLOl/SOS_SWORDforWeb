"use strict";

/**
 * S-OSのキーコード
 */
class SOSKeyCode {

	/**
	 * S-OSのキーコード
	 * CLS
	 * @type {number}
	 */
	static CLS = 0x0C;

	/**
	 * S-OSのキーコード
	 * Enterキー
	 * @type {number}
	 */
	static CR = 0x0D;

	/**
	 * S-OSのキーコード
	 * Breakキー
	 * @type {number}
	 */
	static BRK = 0x1B;
};
